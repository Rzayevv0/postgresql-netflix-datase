CREATE TABLE titles (
    id VARCHAR(20),
    title TEXT,
    type VARCHAR(10),
    description TEXT,
    release_year INTEGER,
    age_certification VARCHAR(10),
    runtime INTEGER,
    genres TEXT,
    production_countries TEXT,
    seasons FLOAT,          
    imdb_id VARCHAR(20),
    imdb_score FLOAT,        
    imdb_votes FLOAT,        
    tmdb_popularity FLOAT,
    tmdb_score FLOAT
);
